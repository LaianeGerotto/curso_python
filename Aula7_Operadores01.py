n1 = int(input('Digite um número: '))
n2 = int(input('Digite mais um número: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma é {}, o Produto é {} e a Divisão é {}'.format(s, m, d))
print('Divisão inteira {} e poteência {}'.format(di, e))

